// condicionais if e else
// numeros
let num = 1;    
if (num === 1) {
    console.log('numero é igual a 1');
} else {  
    console.log('numero é diferente de 1');
}

// idade
let idade = 18;
if (idade >= 18) {
    console.log('Maior de idade');
} else {
    console.log('Menor de idade');
}

// vacinar
let vacina = 15;
if (vacina >= 60) {
    console.log('Pode vacinar');
} else {
    console.log('Não pode vacinar');
}

// dia da semana
let dia = 'segunda';
if (dia === 'domingo') {
    console.log('Vou a praia');
} else {
    console.log('Fico em casa');
}

// hora do dia
let hora = 12;
if (hora < 12) {
    console.log('Bom dia');
} else {
    console.log('Boa tarde');
}

// temperatura
let temp = 30;
if (temp < 20) {
    console.log('Frio');
} else {    
    console.log('Calor');
}

